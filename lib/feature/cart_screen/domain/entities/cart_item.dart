class CartItem {
  final String id;
  final String image;
  final String title;
  final double price;
  final String size;
  final String color;
  final int quantity;

  CartItem({
    required this.id,
    required this.image,
    required this.title,
    required this.price,
    required this.size,
    required this.color,
    required this.quantity,
  });
}
